export function ManualScreen({ onBack }: { onBack: () => void }) {
  const [page, setPage] = useState(0);
  const p = PAGES[page];

  return (
    <AppScreen title="使用手册" onBack={onBack} noPad>
      <div className="flex flex-col h-full">
        <div className="flex-1 overflow-y-auto no-scrollbar px-6 py-6">
          <div className="font-title text-2xl mb-5 txt-accent">{p.title}</div>
          <div className="space-y-3 text-[14px] leading-relaxed txt-dim">
            {p.body.map((line, i) => (
              <p key={i}>{line}</p>
            ))}
          </div>
        </div>
        <div className="px-6 py-4 flex items-center justify-between border-t border-[var(--border)]">
          <button
            onClick={() => setPage((x) => Math.max(0, x - 1))}
            disabled={page === 0}
            className={cls('tap w-10 h-10 rounded-full glass flex items-center justify-center', page === 0 && 'opacity-30')}
          >
            <ChevronLeft size={20} />
          </button>
          <div className="flex items-center gap-1.5">
            {PAGES.map((_, i) => (
              <span
                key={i}
                className={cls('h-1.5 rounded-full transition-all', i === page ? 'w-5 bg-[var(--accent)]' : 'w-1.5 bg-[var(--border)]')}
              />
            ))}
          </div>
          <button
            onClick={() => setPage((x) => Math.min(PAGES.length - 1, x + 1))}
            disabled={page === PAGES.length - 1}
            className={cls('tap w-10 h-10 rounded-full glass flex items-center justify-center', page === PAGES.length - 1 && 'opacity-30')}
          >
            <ChevronRight size={20} />
          </button>
        </div>
      </div>
    </AppScreen>
  );
}
